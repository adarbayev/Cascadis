const Navigation = ({ activeSection, setActiveSection }) => {
  const [isCollapsed, setIsCollapsed] = useState(false);

  const toggleNavigation = () => {
    setIsCollapsed(!isCollapsed);
  };

  const navItems = [
    { id: 'home', label: 'Main Dashboard', icon: <IconDashboard /> },
    { id: 'organisation', label: 'Organisation Structure', icon: <IconOrgStructure /> },
    { id: 'ghg-framework', label: 'GHG Framework', icon: <IconGHGFramework /> },
    { id: 'data-collection', label: 'Data Collection', icon: <IconDataCollection /> },
    { id: 'analysis', label: 'Analysis', icon: <IconAnalysis /> },
    { id: 'climate-risks', label: 'Climate Risks', icon: <IconClimateRisks /> },
    { id: 'reporting', label: 'Reporting', icon: <IconReporting /> },
    { id: 'settings', label: 'Settings', icon: <IconSettings /> },
    { id: 'profile', label: 'My Profile', icon: <IconProfile /> }
  ];

  return (
    <nav className={`navigation ${isCollapsed ? 'collapsed' : ''}`}>
      <div className="nav-header">
        {!isCollapsed && <h2 className="nav-title">Cascadis</h2>}
      </div>
      <ul className="nav-items">
        {navItems.map(item => (
          <li 
            key={item.id} 
            className={`nav-item ${activeSection === item.id ? 'active' : ''}`}
            onClick={() => setActiveSection(item.id)}
            title={isCollapsed ? item.label : ''}
          >
            <span className="nav-icon">{item.icon}</span>
            {!isCollapsed && <span className="nav-label">{item.label}</span>}
          </li>
        ))}
      </ul>
      <div className="nav-footer">
        <button 
          className="collapse-button" 
          onClick={toggleNavigation}
          title={isCollapsed ? "Expand menu" : "Collapse menu"}
        >
          {isCollapsed ? <IconArrowRight /> : <IconArrowLeft />}
        </button>
      </div>
    </nav>
  );
};

export default Navigation; 